const EASH_NAME: &str = "eash";

const EASH_VERSION: &str = "0.0.1";

const EASH_LINK: &str = "github.com/flucium/eash";

const EASH_AUTHOR: &str = "flucium";

pub fn name()->String{
    EASH_NAME.to_owned()
}

pub fn version()->String{
    EASH_VERSION.to_owned()
}

pub fn link()->String{
    EASH_LINK.to_owned()
}

pub fn author()->String{
    EASH_AUTHOR.to_owned()
}
